import { IsOptional, IsString, MaxLength } from 'class-validator';

export class UpdateGroupRoomDto {
  @IsOptional()
  @IsString()
  @MaxLength(255)
  name?: string;

  @IsOptional()
  @IsString()
  @MaxLength(2000)
  description?: string;

  @IsOptional()
  @IsString()
  @MaxLength(500)
  avatar?: string;
}
